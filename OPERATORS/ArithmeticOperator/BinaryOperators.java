package OPERATORS.ArithmeticOperator;

public class BinaryOperators {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println("Add="+(a+b));

        int x=10;
        int y=5;
        System.out.println("Subtract="+(x-y));     //THESE ARE BINARY OPERATORS

        int u=10;
        int v=7;
        System.out.println("Multiply="+(u*v));


        int i=70;
        int j=5;
        System.out.println("Devide="+(i/j));


        int l=76;
        int m=5;
        System.out.println("modulo (remender)="+(l%m));
    }
}
