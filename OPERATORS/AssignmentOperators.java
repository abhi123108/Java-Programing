package OPERATORS;
import java.util.*;
public class AssignmentOperators {
    public static void main(String[] args) {
        int A=5;
        int B=A;
        System.out.println(B);


        int a=23;
        a+=7;   //a=a+7
        System.out.println(a);
        

        int U=33;
        U-=7;
        System.out.println(U);


        int u=20;
        u*=5;
        System.out.println(u);


        int V=20;
        V/=5;
        System.out.println(V);
    }
}
