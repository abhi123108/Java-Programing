package OPERATORS;

public class RelationalOperators {
    public static void main(String[] args) {
        int A=10;
        int B=12;
        System.out.println(A==B);

        int a=10;
        int b=10;
        System.out.println(a==b);


        int U=10;
        int V=12;                      
        System.out.println(U!=V);
                                 //(!= means "not equal to")
        int u=10;
        int v=10;                       
        System.out.println(u!=v);


        int I=12;
        int J=23;
        System.out.println(I>J);

        int i=12;
        int j=23;
        System.out.println(i<j);


        int S=12;
        int T=23;
        System.out.println(S>=T);

        int s=12;
        int t=23;
        System.out.println(s<=t);
    }
}
