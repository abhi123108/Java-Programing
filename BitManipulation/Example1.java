package BitManipulation;

public class Example1 {
    public static void main(String[] args) {
        System.out.println(5&6);   //& operator

        System.out.println(5|6);   // OR operator

        System.out.println(5^6);     //XOR operator

        System.out.println(~5);    //NOT operator

        System.out.println(5<<2);  //Binary Left Shift operator

        System.out.println(6>>1);  //Binary Right Shift operator
    }
}
