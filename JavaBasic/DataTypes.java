public class DataTypes {
    public static void main(String[] args) {
        //Datatypes
        byte b=4;
        System.out.println(b);
        char ch='a';
        System.out.println(ch);
        boolean var=true;
        System.out.println(var);
        int number=5;
        System.out.println(number);
        short n =240;
        System.out.println(n);
        float f = 34;
        System.out.println(f);
    }
}
